let colores = ["verde", "azul", "amarillo", "rojo"];
let colorfinal = "";

for (let c of colores) {
  console.log(
    `El color final es:  ${colorfinal} con longitud ${colorfinal.length} color de arreglo es ${c} con longitud ${c.length}`
  );
  if (colorfinal.length < colorfinal.length) {
    colorfinal = c;
  }
}

console.log(colorfinal);
